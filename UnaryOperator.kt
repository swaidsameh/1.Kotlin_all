fun main() {
    var a = 2

//    positiv
    println(+a)

//    negative
    println(-a)

//    not
    val b = true
    println(!b)

//    pre increment
    a = 5
    print("pre increment  ")
    println(++a)

//    pre decrement
    a = 5
    print("pre decrement  ")
    println(--a)

//    post increment
    a = 5
    print("post increment  ")
    print(a++)
    print("  ")
    println(a)
//    post decrement
    a = 5
    print("post decrement  ")
    print(a--)
    print("  ")
    println(a)

    println()
}