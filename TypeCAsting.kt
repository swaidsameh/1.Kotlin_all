fun main(args: Array<String>) {
    var a : Int = 10
    var b : Int = 540000
    println(a)
    println("--")

    //int to double
    var c:Double = a.toDouble()
    println(c)
    println(a.toDouble())
    println("--")

    //String to int
    var str : String = "10"
    println(str)
    println(str.toInt())
    println("--")

    //int to Byte
    var d : Int = 100
    var e:Byte = d.toByte()
    println(d)
    println(e)
    println(e.toDouble())
    println("--")


}